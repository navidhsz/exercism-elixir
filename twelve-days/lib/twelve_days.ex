  @doc """
  Given a `number`, return the song's verse for that specific day, including
  all gifts for previous days in the same line.
  """
  @spec verse(number :: integer) :: String.t()
  def verse(number) do
    @dayToVerse |> Map.fetch!(number)
  end

  @doc """
  Given a `starting_verse` and an `ending_verse`, return the verses for each
  included day, one per line.
  """
  @spec verses(starting_verse :: integer, ending_verse :: integer) :: String.t()
  def verses(starting_verse, ending_verse) do
    verses_num = starting_verse..(ending_verse - 1) |> Enum.to_list()

    verses =
      (verses_num |> Enum.map(fn verse_num -> verse(verse_num) <> "\n" end)) ++
        [verse(ending_verse)]

    verses |> Enum.reduce("", fn verse, acc -> acc <> verse end)
  end

  @doc """
  Sing all 12 verses, in order, one verse per line.
  """
  @spec sing() :: String.t()
  def sing do
    verses(1, @dayToVerse |> Map.size())
  end
end
